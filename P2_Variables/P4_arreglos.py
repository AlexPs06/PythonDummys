# Ejemplo de arreglos y tipos de datos en Python

# Arreglo de variables enteras [int]
Arreglo_enteros = [1,2,3]
print("Arreglo enteros:", Arreglo_enteros)

# Arreglo de variables de cadena de texto [str]
Arreglo_cadenas = ["1","2","3"]
print("Arreglo cadenas:", Arreglo_cadenas)

# Arreglo de variables [str]
Arreglo_mix = [1,"2",3]
print("Arreglo mix:", Arreglo_mix)
