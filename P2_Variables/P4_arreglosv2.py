# Ejemplo de arreglos y tipos de datos en Python

# Accediendo a los valores de los arreglos de variables enteras [int]
Arreglo_enteros = [1,2,3]

#Impresion de la primera posicion del arreglo
print("Primera posicion del arreglo enteros:", Arreglo_enteros[0])

#Impresion de la segunda posicion del arreglo
print("Segunda posicion del arreglo enteros:", Arreglo_enteros[1])

#Impresion de la tercera posicion del arreglo
print("Tercera posicion del arreglo enteros:", Arreglo_enteros[2])

#Impresion de la ultima posicion del arreglo sin importar el tama√±o
print("Siempre obtenemos la ultima posicion del arreglo:", Arreglo_enteros[-1])

#Impresion del arreglo en forma inversa
print("imprimimos el arreglo de forma inversa:", Arreglo_enteros[::-1])

#Impresion del arreglo en forma normal
print("imprimimos el arreglo de forma normal:", Arreglo_enteros)

