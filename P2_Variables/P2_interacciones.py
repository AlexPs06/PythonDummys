# Operaciones con enteros
# Suma, resta, multiplicacion, division de enteros. (int)
a = 25
b = 10
print("Operaciones con enteros:")
print("a =",a)
print("b =",b)

c=a+b
print("Suma:",c)  # Suma
c=a-b
print("Resta:",c)  # Resta
c=a*b
print("Multiplicación:",c)  # Multiplicación
c=a/b
print("División:",c)  # División


